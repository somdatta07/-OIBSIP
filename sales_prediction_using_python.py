from sklearn.metrics import (
    mean_absolute_error,
    mean_squared_error,
    r2_score
)

# ==========================================
# LOAD DATASET
# ==========================================

print("="*50)
print("LOADING DATASET")
print("="*50)

df = pd.read_csv("Advertising.csv")

print(df.head())

import zipfile

# Unzip the archive.zip file
